package com.kodnest.concat;

public class Concatenation1 {
	public static void main(String[] args) {
          String s1 = "java";
          String s2 = "Program";
          String s3 = s1+s2;
          String s4 = s1.concat(s2);
          
          System.out.println(s3);
          System.out.println(s4);
	}       
}
