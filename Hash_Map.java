package com.kodnest.jfc;

import java.util.HashMap;

public class Hash_Map {
	public static void main(String[] args) {
		HashMap<Integer,String> hashmap = new HashMap<Integer,String>();
		hashmap.put(1, "Java");
		hashmap.put(5, "HTML");
		hashmap.put(3, "CSS");
		hashmap.put(8, "JS");
		hashmap.put(4, "C");
		System.out.println(hashmap);
		
	}

}
